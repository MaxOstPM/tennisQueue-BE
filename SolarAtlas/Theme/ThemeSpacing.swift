import SwiftUI

/// Spacing constants (4px base grid) for consistent padding/margins
extension CGFloat {
    static let spaceXS: CGFloat = 4    // extra small spacing
    static let spaceSM: CGFloat = 8    // small spacing
    static let spaceMD: CGFloat = 12   // medium/default spacing
    static let spaceLG: CGFloat = 16   // large spacing
    static let spaceXL: CGFloat = 24   // extra large spacing
    static let space2XL: CGFloat = 32  // double extra large (screen margins)
}
